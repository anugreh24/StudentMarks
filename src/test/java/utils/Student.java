package utils;

public class Student {

	String name;
	double maths_score;
	double science_score;
	double language_score;
	double percentage;

	public Student(String name, double math, double science, double language, double percentage) {

		this.name = name;
		this.maths_score = math;
		this.science_score = science;
		this.language_score = language;
		this.percentage = percentage;

	}

}
